package me.manishmahalwal.android.fms2;

public class User {

    public String name, email, pass, number, rollNo, gender;

    public User(String name, String email, String pass, String number, String rollNo, String gender) {
        this.name = name;
        this.email = email;
        this.pass = pass;
        this.number = number;
        this.rollNo = rollNo;
        this.gender = gender;
    }
}
